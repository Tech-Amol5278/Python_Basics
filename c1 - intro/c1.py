### Escape sequences  #####

#  single quote - \'
print('I\'m amol')
#  double quote - \\'
print("I\"m amol" )
#  backslash - \\
print("I am\\amol")
#  new line - \n
print("I am amol \n how are you")
#  tab - \t
print("I am amol\tHi\tHow are you")
#  backspace - \b
print("I am amol\bHi")
###############################################################
